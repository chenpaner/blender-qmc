def rgb_to_hex(rgb):
    """Convert RGB tuple to HEX string."""
    return '#{:02x}{:02x}{:02x}'.format(*rgb)

def convert_rgb_to_hex(rgb_list):
    """Convert a list of RGB tuples to HEX."""
    hex_colors = []
    for rgb in rgb_list:
        hex_colors.append(rgb_to_hex(rgb))
    return hex_colors

